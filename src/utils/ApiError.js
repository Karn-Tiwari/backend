//Humne API error files bana li hai iska matlab jab bhi hme errors ayenge to ise format me ayenge

class ApiError extends Error {
  constructor(
    statusCode,
    message = "Something went wrong",
    errors = [],
    stack = ""
  ) {
    super(message);
    this.statusCode = statusCode;
    this.data = null;
    this.message = message;
    this.success = false;
    this.errors = errors;

    //ye neeche wale code ko ignore bhi kar sakte ho utna jarori nhi hai

    if (stack) {
      this.stack = stack;
    } else {
      Error.captureStackTrace(this, this.constructor);
    }
  }
}

export { ApiError };
